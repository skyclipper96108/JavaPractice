package cn.tree;
import cn.set.map;

import java.util.ArrayList;

public class AVLTree<K extends Comparable<K>,V> implements map<K, V> {

	private class Node{
		public K key;
		public V value;
		public Node left, right;
		public int height;

		public Node(K key, V value) {
			this.key=key;
			this.value=value;
			left=null;
			right=null;
		}
	}
	private Node root;
	private int size;
	public AVLTree() {
		root=null;
		size=0;
	}
	//判断二叉树是否是一棵二分搜索树
	public boolean isBST(){
		ArrayList<K> keys = new ArrayList<>();
		inOrder(root, keys);
		for(int i=1; i<keys.size();i++){
			if(keys.get(i-1).compareTo(keys.get(i))>0){
				return false;
			}
		}
		return true;
	}

	public boolean isBalanced(){
		return isBalanced(root);
	}

	private boolean isBalanced(Node node){
		if(node==null){
			return true;
		}
		//左右子树差值
		int balanceFactor = getBalanceFactor(node);
		if(Math.abs(balanceFactor)>1)
			return false;
		return isBalanced(node.left)&&isBalanced(node.right);
	}

	private void inOrder(Node node, ArrayList<K> keys) {
		//二分搜索树中序遍历为排序的数组
		if(node == null){
			return;
		}
		inOrder(node.left, keys);
		keys.add(node.key);
		inOrder(node.right,keys);

	}

	//获得节点node的高度
	private int getHeight(Node node){
		if(node==null){
			return 0;
		}
		return node.height;
	}
	//获得节点node的平衡因子
	private int getBalanceFactor(Node node){
		if(node==null){
			return 0;
		}
		return getHeight(node.left)-getHeight(node.right);
	}
	@Override
	public void add(K key, V value) {
		root = add(root, key, value);

	}
	private Node add(Node node, K key, V value) {
		if(node==null) {
			size++;
			return new Node(key, value);
		}
		if(key.compareTo(node.key)<0)
			node.left=add(node.left, key, value);
		else if(key.compareTo(node.key)>0)
			node.right=add(node.right,key, value);
		else node.value=value;

		//更新height
		node.height = 1 +Math.max(getHeight(node.left),getHeight(node.right));

		//计算平衡因子
		int balanceFactor = getBalanceFactor(node);
		if(Math.abs(balanceFactor)>1){
			System.out.println("unbalanced: "+balanceFactor);
		}
		//LL
		if(balanceFactor>1&&getBalanceFactor(node.left)>=0){
			//左侧不平衡 右旋转
			return rightRotate(node);
		}
		//RR
		if(balanceFactor<-1&&getBalanceFactor(node.right)<=0){
			//左侧不平衡 右旋转
			return leftRotate(node);
		}
		//LR
		if(balanceFactor>1&&getBalanceFactor(node.left)<0){
			//转化为LL问题
			node.left = leftRotate(node.left);
			return rightRotate(node);
		}
		//RL
		if(balanceFactor<-1&&getBalanceFactor(node.right)<0){
			//转化为RR问题
			node.right = rightRotate(node.right);
			return leftRotate(node);
		}
		return node;
	}
	//对节点y进行向右旋转操作，返回旋转后的新根节点x
	//       y                          x
	//      / \                       /   \
	//     x   T4   向右旋转（y）     z     y
	//	  / \     ----------------> / \    / \
	//   z   T3                    T1  T2 T3  T4
	//  / \
	// T1  T2
	private Node rightRotate(Node y){
		Node x = y.left;
		Node T3 = x.right;
		//向右旋转过程
		x.right = y;
		y.left = T3;
		//更新height
		y.height = Math.max(getHeight(y.left),getHeight(y.right))+1;
		x.height = Math.max(getHeight(x.left),getHeight(x.right))+1;
		return x;
	}

	private Node leftRotate(Node y){
		Node x = y.right;
		Node T3 = x.left;
		//向左旋转过程
		x.left = y;
		y.right = T3;
		//更新height
		y.height = Math.max(getHeight(y.left),getHeight(y.right))+1;
		x.height = Math.max(getHeight(x.left),getHeight(x.right))+1;
		return x;
	}
	// 返回以node为根节点的二分搜索树中key所在节点
	private Node getNode(Node node, K key) {
		if(node==null)return null;
		if(key.compareTo(node.key)==0)return node;
		else if(key.compareTo(node.key)<0)
			return getNode(node.left, key);
		else return getNode(node.right,key);
	}
	public K minimum() {
		if(size==0)throw new IllegalArgumentException("BST is empty");
		return minimum(root).key;

	}
	private Node minimum(Node node) {
		if(node.left==null)return node;
		return minimum(node.left);
	}
	public K removeMin() {
		K ret = minimum();
		root = removeMin(root);
		return ret;
	}
	private Node removeMin(Node node) {
		if(node.left==null) {
			Node rightnode=node.right;
			node.right=null;
			size--;
			return rightnode;
		}
		node.left=removeMin(node.left);
		return node;
	}
	// 返回删除节点后新的二分搜索树的根
	private Node remove(Node node, K key) {
		if(node==null)return null;
		Node retNode;
		if(key.compareTo(node.key)<0) {
			node.left=remove(node.left, key);
			retNode = node;
		}else if(key.compareTo(node.key)>0) {
			node.right=remove(node.right, key);
			retNode = node;
		}else {
			if(node.left==null) {
				Node rightnode=node.right;
				node.right=null;
				size--;
				retNode = rightnode;
			}else if(node.right==null) {
				Node leftnode=node.left;
				node.left=null;
				size--;
				retNode = leftnode;
			}else {
				// 待删除的节点左右节点均不为空
				// 找到比待删除节点大的最小节点，即待删除节点右子树的最小节点
				Node successor = minimum(node.right);
//				successor.right=removeMin(node.right);
				successor.right = remove(node.right, successor.key);
				successor.left = node.left;
				node.left = node.right = null;
				retNode = successor;
			}
		}
		if(retNode==null){
			return null;
		}
		//更新height
		retNode.height = 1 +Math.max(getHeight(retNode.left),getHeight(retNode.right));

		//计算平衡因子
		int balanceFactor = getBalanceFactor(retNode);
		if(Math.abs(balanceFactor)>1){
			System.out.println("unbalanced: "+balanceFactor);
		}
		//LL
		if(balanceFactor>1&&getBalanceFactor(retNode.left)>=0){
			//左侧不平衡 右旋转
			return rightRotate(retNode);
		}
		//RR
		if(balanceFactor<-1&&getBalanceFactor(retNode.right)<=0){
			//左侧不平衡 右旋转
			return leftRotate(retNode);
		}
		//LR
		if(balanceFactor>1&&getBalanceFactor(retNode.left)<0){
			//转化为LL问题
			retNode.left = leftRotate(retNode.left);
			return rightRotate(retNode);
		}
		//RL
		if(balanceFactor<-1&&getBalanceFactor(retNode.right)<0){
			//转化为RR问题
			retNode.right = rightRotate(retNode.right);
			return leftRotate(retNode);
		}
		return retNode;
	}
	@Override
	public V remove(K key) {
		Node node = getNode(root, key);
		if(node!=null) {
			root=remove(root, key);
			return node.value;
		}
		return null;
	}
	@Override
	public boolean contains(K key) {
		return getNode(root, key)!=null;
	}
	@Override
	public V get(K key) {
		Node node = getNode(root, key);
		return node==null?null:node.value;
	}
	@Override
	public void set(K key, V newValue) {
		Node node=getNode(root, key);
		if(node==null)throw new IllegalArgumentException(key+"doesn't exist");
		node.value=newValue;
	}
	@Override
	public int getSize() {
		return size;
	}
	@Override
	public boolean isEmpty() {
		return false;
	}



}
